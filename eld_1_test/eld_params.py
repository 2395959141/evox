import numpy as np

class ELDProblem:
    def __init__(self, Pd, Pmin, Pmax, a, b, c, g, h):
        self.Pd = Pd
        self.Pmin = np.array(Pmin)
        self.Pmax = np.array(Pmax)
        self.a = np.array(a)
        self.b = np.array(b)
        self.c = np.array(c)
        self.g = np.array(g)
        self.h = np.array(h)

    def __str__(self):
        return (f"Pd: {self.Pd}\n"
                f"Pmin: {self.Pmin}\n"
                f"Pmax: {self.Pmax}\n"
                f"a: {self.a}\n"
                f"b: {self.b}\n"
                f"c: {self.c}\n"
                f"g: {self.g}\n"
                f"h: {self.h}")

